package it.polimi.margoni.beans;

import java.util.Date;

public class ImageBeanRIA
{
	private String path;
	private int albumid;
	private String titleIm;
	private String descriptionIm;
	private Date dateIm;
	

	
	
	
	
	public String getPath()
	{
		return path;
	}
	public void setPath(String path)
	{
		this.path = path;
	}
	public int getAlbumid()
	{
		return albumid;
	}
	public void setAlbumid(int albumid)
	{
		this.albumid = albumid;
	}
	
	public Date getDateIm()
	{
		return dateIm;
	}
	public void setDateIm(Date dateIm)
	{
		this.dateIm = dateIm;
	}
	public String getTitleIm()
	{
		return titleIm;
	}
	public void setTitleIm(String titleIm)
	{
		this.titleIm = titleIm;
	}
	public String getDescriptionIm()
	{
		return descriptionIm;
	}
	public void setDescriptionIm(String descriptionIm)
	{
		this.descriptionIm = descriptionIm;
	}
	

	
	

	}
	
	
	
	
	

